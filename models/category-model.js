import { Schema, model } from "mongoose";

const categorySchema = new Schema({
    title: {
        type: String,
        required: true,
        unique: true
    },
    description: {
        type: String,
        required: true,
    },
    imagePath: {
        type: String,
        required: true
    },
    isDeleted: {
        type: Boolean,
        default: false
    },
    createdAt: {
        type: Date,
        default: Date.now,
    }
})

const CategoryModel = model('category', categorySchema, 'categories');

export default CategoryModel;   